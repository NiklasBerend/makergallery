	<h2>Über das Exponat</h2>
    <p style="text-align: justify">Ein Raspberry Pi ist ein kostengünstiger Kleinstcomputer. Er eignet sich gut, um spielerisch einige IT-Grundlagen (Betriebssystem, Befehle, Terminal …)  kennenzulernen, um mit Technik zu experimentieren und zu basteln. 
Im Tüftellabor Hamburg haben (u.a.) Anfänger_innen den Raspberry getestet: In ihrer Schritt-für-Schritt-Anleitung <a href="http://goo.gl/TLECn8" target="_blank">“RaspberryMinecraft für Dummies”</a> geben sie ihre Erfahrungen weiter, erklären in einfachster Sprache, welche Taste, wann gedrückt werden soll. Neben der ersten Inbetriebnahme des Raspberry Pi wird auch die Installation des Computerspiels Minecraft und dessen Inbetriebnahme beschrieben.
<br />
Der Kaufpreis liegt bei 35 Euro; das Zubehör ist meistens von anderen Geräten bereits vorhanden: SD-Karte, Netzteil, Maus, Tastatur, Bildschirm, HDMI-Kabel/-Adapter).
