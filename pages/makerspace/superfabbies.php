    <p style="text-align: justify">Geometrische Faltkunst aus Papier - die Kunst daran ist, dass der LaserCutter das Papier nicht schneidet, sondern eine Falz entsteht. Durch das Lasern auf der Vorder- und Rückseite des Blattes entstehen ganz neue dreidimensionale Formen. Unsere Ergebnisse sind erstmal zweckfrei, es kommen aber schnell Ideen zur Gestaltung von Lampen, Möbeln, Schmuck, etc. </p>

       
